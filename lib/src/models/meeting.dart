class Meeting {
  int meetingID;
  String type;
  String start;
  String end;
  String daysOfTheWeek;
  String roomNumber;
  String buildingName;

  Meeting({
    required this.meetingID,
    required this.type,
    required this.start,
    required this.end,
    required this.daysOfTheWeek,
    required this.roomNumber,
    required this.buildingName,
  });
}
